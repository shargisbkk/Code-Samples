public class Point {
    
    private int width;
    private int height;
    
    public Point(int theWidth, int theHeight){
        width = theWidth;
        height = theHeight;
    }
    
    public int getWidth(){
        return width;
    }
    public int getHeight(){
        return height;
    }
    
    public void setWidth(int theWidth){
        width = theWidth;
    }
    public void setHeight(int theHeight){
        height = theHeight;
    }
    
}
